    <h3>What is blockchain?</h3>

    <p>If you wanted to send money to someone, a bank needs to verify that transaction and it could take days. With cryptocurrency, it is faster as no banks are involved because blockchain technology allows for a record or ledger for every crypto transaction ever made to be stored not in one place but across vast numbers of computers. That is what is meant by decentralised. This process means that blockchain is secure and transparent.</p>


