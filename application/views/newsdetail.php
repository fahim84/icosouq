            <div class="post-full-text contents" itemprop="articleBody"> 
                <p>Logistics giant DHL announced it had partnered with <a href="" target="_blank">Accenture</a> and <a href="" rel="nofollow,noopener" target="_blank">created</a> a Blockchain-based supply chain prototype March 12.
                </p>
                <p dir="ltr">In a joint press release, the two companies presented a trend report for <a href="https://cointelegraph.com/tags/blockchain" target="_blank">Blockchain</a> in tandem with what appears to be ongoing plans to introduce the technology to global pharmaceutical supply.</p>
                <p dir="ltr">“DHL and Accenture created a blockchain-based serialization prototype with nodes in six geographies to track pharmaceuticals across the supply chain,” the release reports.</p>
                <blockquote>
                    <p dir="ltr">“The ledger tracking these medicines may be shared with stakeholders, including manufacturers, warehouses, distributors, pharmacies, hospitals, and doctors. Lab-simulations show how blockchain could handle more than seven billion unique serial numbers and 1,500 transactions per second.”</p>
                </blockquote>
                <p dir="ltr">Permissioned Blockchains have formed a continuing area of interest for Accenture over the past year.</p>
                <p dir="ltr">In September 2017, the company <a href="https://cointelegraph.com/news/accenture-secures-patent-for-its-editable-blockchain-technology" target="_top">filed a patent</a> for a so-called ‘editable Blockchain,’ allowing “enterprises to resolve human errors, accommodate legal and regulatory requirements, and address mischief and other issues, while preserving key cryptographic features.”</p>
                <p dir="ltr">While the concept of manually altering Blockchain data sounds counterintuitive for a technology in which an immutable ledger lies at the heart of its effectiveness, the DHL scheme appears equipped to tackle issues endemic in the pharmaceutical industry such as “tampering.”</p>
                <p dir="ltr">“We see especially exciting potential for blockchain in pharmaceuticals, which is why we focused our proof of concept with Accenture on the life sciences and healthcare industry,” Keith Turner, CIO at DHL Supply Chain commented.</p>
                <blockquote>
                    <p dir="ltr">“By utilizing the inherent irrefutability within blockchain technologies, we can make great strides in highlighting tampering, reducing the risk of counterfeits and actually saving lives.”</p>
                </blockquote> 
